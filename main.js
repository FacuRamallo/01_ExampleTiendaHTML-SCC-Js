function OpenCatList(idCat){
    let categori= idCat;
    if (categori == 00) {
        catArrayToDisplay = catTartas;
    } else if (categori == 01) {
        catArrayToDisplay= catBolleria;
    } else {
        catArrayToDisplay = catPanes;
    }
    
    let mainCategories = document.getElementById("main_categories"); 
    mainCategories.classList.toggle('displaynone'); //agrego clase "displaynone" a section mainCategories para que deje verse en pantalla
    ReadArrayAndPrintbySectionID(catArrayToDisplay,AddCardscodebyID,"main_ListProducts");
    
}



